"""
    函数参数
        实际参数
"""


def fun01(a, b, c):
    print(a)
    print(b)
    print(c)


# 1. 位置实参：根据位置，将实参传递给形参。
fun01(1, 2, 3)

# 2.序列实参：使用星号将序列拆分后，与形参进行对应。
list01 = [4, 5, 6]
fun01(*list01)

# 3.关键字:根据名称，将实参传递给形参。
fun01(a=1, c=3, b=2)

# 4. 字典实参：使用双星号将字典拆分后，与形参进行对应。
dict01 = {"c": 3, "b": 2, "a": 1}
fun01(**dict01)
